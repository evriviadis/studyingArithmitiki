% Με τη χρήση συμβολικών υπολογισμών να υπολογίσετε την παράγωγο της
% συνάρτησης f(x). Να σχεδιάσετε τις γραφικές παραστάσεις των f(x) και f'(x)
% στους ίδιους άξονες.

%% 1. Ορισμός Συνάρτησης και Παραγώγου
% Ορισμός της συνάρτησης f(x)
% Υπολογισμός της παραγώγου f'(x)
syms x;
f(x) = (x * sin(x)) / (1 + x^2);
df(x) = diff(f(x), x);

%% 2. Γραφική Παράσταση
figure;

% Σχεδίαση της f(x)
fplot(f(x),   'DisplayName', 'f(x)', 'LineWidth', 1.5);
hold on;

% Σχεδίαση της παραγώγου f'(x)


fplot(df(x), [-10, 10], 'DisplayName', 'df(x)/dx', 'LineWidth', 1.5);
hold off;

%% 3. Μορφοποίηση Γραφήματος

legend('show', 'Location', 'best');
title('Γραφική Παράσταση των f(x) και f''(x)');
xlabel('x');
ylabel('y');
grid on;





% Ένα τρένο κινείται με ταχύτητα u = t^2 + 0.5t + 2 (m/s).
% Να υπολογίσετε την απόσταση που θα διανύσει από t=1 έως t=10 sec.
% (Η απόσταση είναι το ορισμένο ολοκλήρωμα της ταχύτητας)

u = @(t) t.^2 + 0.5*t + 2;

% Υπολογισμός ορισμένου ολοκληρώματος (απόσταση = ∫ u dt) από t=1 έως t=10
distance_sym = integral(u, 1, 10);

% Μετατροπή σε αριθμητική τιμή
distance = double(distance_sym);

fprintf('Η απόσταση που θα διανύσει το τρένο είναι: %f μέτρα\n', distance);

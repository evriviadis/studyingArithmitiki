% ask2.m
% Επίλυση συστήματος μη γραμμικών εξισώσεων χρησιμοποιώντας την fsolve.
%
% Το σύστημα είναι:
% x^3 + y^2 = 1     =>  x^3 + y^2 - 1 = 0
% exp(-x) - y = 2   =>  exp(-x) - y - 2 = 0

% Ορισμός της συνάρτησης του συστήματος.
% Το v είναι το διάνυσμα των αγνώστων, όπου v(1) = x και v(2) = y.
F = @(v) [v(1)^3 + v(2)^2 - 1; 
          exp(-v(1)) - v(2) - 2];

% Αρχική πρόβλεψη που δίνεται από την εκφώνηση
x0 = [0, 0];

% Επίλυση
% Μπορούμε να χρησιμοποιήσουμε options για να μην εμφανίζει πολλά μηνύματα (προαιρετικά)
options = optimoptions('fsolve', 'Display', 'iter');
[sol, fval] = fsolve(F, x0, options);

% Εμφάνιση αποτελεσμάτων
fprintf('\n--- Αποτελέσματα ---\n');
fprintf('Οι ρίζες του συστήματος είναι:\n');
fprintf('x = %f\n', sol(1));
fprintf('y = %f\n', sol(2));

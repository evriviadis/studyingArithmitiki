% Άσκηση 2
K = 1000;
n = 20;
A = 24300;

% Εξίσωση: f(i) = 0
% Προσοχή: Η συνάρτηση fzero χρειάζεται μια συνάρτηση που μηδενίζεται
f = @(i) K * ((1 + i).^n - 1) ./ i - A;

% Εύρεση ρίζας στο διάστημα [0.005, 0.10] (από 0.5% έως 10%)
i_sol = fzero(f, [0.005, 0.10]);

% Εμφάνιση του αποτελέσματος με ακρίβεια 6 δεκαδικών ψηφίων
fprintf('Το επιτόκιο καταθέσεων είναι: %.6f (δηλαδή περίπου %.2f%%)\n', i_sol, i_sol * 100);

% Άσκηση 4
R = [5 4 4 1 2 3 5 4;
     4 5 3 2 1 2 5 4;
     1 1 2 4 4 5 1 2;
     3 2 1 5 4 5 2 1;
     2 3 4 2 3 3 4 4;
     5 4 4 1 2 1 5 4];

% Ανάλυση Ιδιαζουσών Τιμών (SVD)
[U, S, V] = svd(R);

% Εξαγωγή των ιδιάζουσων τιμών από τον διαγώνιο πίνακα S
singular_values = diag(S);

% Εύρεση των σημαντικών συνιστωσών (ιδιάζουσες τιμές > 2)
k = sum(singular_values > 2);
fprintf('Αριθμός σημαντικών συνιστωσών (ιδιάζουσες τιμές > 2): %d\n', k);

% Ανακατασκευή του πίνακα διατηρώντας μόνο τις k σημαντικές συνιστώσες
R_approx = U(:, 1:k) * S(1:k, 1:k) * V(:, 1:k)';

disp('Οι ιδιάζουσες τιμές είναι:');
disp(singular_values');

disp('Ο προσεγγιστικός πίνακας με τις σημαντικές συνιστώσες:');
disp(R_approx);

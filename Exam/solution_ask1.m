function solution_ask1()
    % Exercise 1
    % Να λύσετε το ακόλουθο σύστημα εξισώσεων. Εισάγετε την απάντησή σας σε
    % ένα αρχείο με όνομα ask1.m
    % x + e^x = -y^2  => x + e^x + y^2 = 0
    % x + x^2 + y - 4 = 0

    % Ορίζουμε το σύστημα των εξισώσεων ως μία ανώνυμη συνάρτηση
    % u(1) αντιστοιχεί στο x
    % u(2) αντιστοιχεί στο y
    F = @(u) [u(1) + exp(u(1)) + u(2)^2; ...
              u(1) + u(1)^2 + u(2) - 4];

    % Αρχική εκτίμηση (initial guess) για τις ρίζες [x0, y0]
    u0 = [0; 0];

    % Επίλυση με χρήση της έτοιμης συνάρτησης fsolve του MATLAB
    % Η επιλογή 'Display', 'iter' προαιρετικά εμφανίζει την πορεία της λύσης
    options = optimoptions('fsolve', 'Display', 'iter');
    [sol, fval] = fsolve(F, u0, options);

    % Εμφάνιση των τελικών αποτελεσμάτων
    fprintf('\nΛύση του συστήματος:\n');
    fprintf('x = %f\n', sol(1));
    fprintf('y = %f\n', sol(2));
end
